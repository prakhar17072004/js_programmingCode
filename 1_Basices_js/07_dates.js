// Dates

let myDate = new Date()
console.log(myDate);// give in standard way

console.log(myDate.toString());//give date in string
console.log(myDate.toLocaleString())// give it in local understanding
console.log(myDate.getDate());// only give present day
console.log(myDate.getDay());// give day of week
console.log(myDate.getFullYear());// give year
console.log(myDate.getHours());// give hours of standard clock




